package com.javabasic.concepts;

public class Tutorial4WhileLoop {

	public static void main(String[] args) {
		System.out.println("This While Loop Concept");
		
		int value = 0;
		
		while (value < 5)
		{
			System.out.println("While Loop condition is True & the value is:  " + value);
			value = value +2;
		}

	}

}
